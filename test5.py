import math # Para chamar um ou dois métodos específicos digite: "from math import (método), (método)"

num = int(input('Digite um número: '))
raiz = math.sqrt(num) 

print('A raiz quadrada de {} é {:.2f}' .format(num, raiz))

# "math.sqrt" - Raiz Quadrada | "math.ceil" - Arredonda para Cima | "math.floor" - Arredonda para Baixo |