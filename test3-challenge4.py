v1 = (input('Digite algo aqui: '))

print('A variável armazenada é do tipo', type(v1))
print('A variável é númerica:', v1.isnumeric()) # Comando que diz se o valor da variável é numérica.
print('A variável é algarismos:', v1.isalpha()) # Comando que diz se o valor da variável é algarismo.
print('A variável é alphanumeric:', v1.isalnum()) # Comando que diz se o valor da variável é algarismo ou numérica.
print('A variável está em maiúscula:', v1.isupper()) # Comando que diz se o valor da variável está em maiúsculo.
print('A variável está em minúscula:', v1.islower()) # Comando que diz se o valor da variável está em minúsculo.